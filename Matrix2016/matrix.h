// Функция чтения матрицы вещественных чисел из текстового файла с заданным именем.
// Динамическое выделение памяти.
// Входные параметры:
//     fname - имя файла.
// Выходные параметры:
//     a - указатель на начало массива;
//     m - количество строк;
//     n - количество количество столбцов.
